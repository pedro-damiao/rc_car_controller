/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  LL_APB2_GRP1_EnableClock(LL_APB2_GRP1_PERIPH_SYSCFG);
  LL_APB1_GRP1_EnableClock(LL_APB1_GRP1_PERIPH_PWR);

  /* System interrupt init*/
  NVIC_SetPriorityGrouping(NVIC_PRIORITYGROUP_0);

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_SPI3_Init();
  MX_ADC1_Init();
  /* USER CODE BEGIN 2 */
  Gpio led(GPIOA, LD2_Pin); // Example GPIO pin for LED

  Gpio rf_t_spi_ncs(GPIOB, LL_GPIO_PIN_12); // Example GPIO pin for SPI CS
  Gpio rf_t_enable(GPIOB, LL_GPIO_PIN_13); // Example GPIO pin for RF CE

  Spi rf_t_spi(SPI3, rf_t_spi_ncs);

  TransceiverRF rf_t_nRF24L01(rf_t_spi, rf_t_enable);

  HW_ADC joystick_adc(ADC1, HW_ADC::Mode::Scan, 2);

  Mw_Joystick joystick(joystick_adc);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */
    if(timer>1000) {
      led.toggle();
      timer=0;
    }
    else if(nrf_it_flag) {
      if(rf_t_nRF24L01.get_mode() == TransceiverRF::Mode::RX) {
        rf_t_nRF24L01.rx_irq(rx_data);
        printf("Received data: ");
        for(int i = 0; i < NRF24L01P_PAYLOAD_LENGTH; i++)
          printf("%02X ", rx_data[i]);
      }
      else if(rf_t_nRF24L01.get_mode() == TransceiverRF::Mode::TX) {
        rf_t_nRF24L01.tx_irq();
      }
      nrf_it_flag = 0;
    }
    else { // TODO: ADD another timer
      joystick.update();
      uint16_t x = joystick.getX();
      uint16_t y = joystick.getY();
      printf("Joystick X: %u, Y: %u\r\n", x, y);
    }
    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

